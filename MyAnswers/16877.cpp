vector<int> fib;

class SpragueGrundy {
    int n;
    std::vector<int> grundyNumbers;

public:
    explicit SpragueGrundy(int size) : n(size), grundyNumbers(size + 1, 0) {}

    void calc_set() {
        for (int i = 1; i <= n; i++) {
            set<int> s;
            for (const auto &f : fib) {
                if (f > i) break;
                s.emplace(grundyNumbers[f] ^ grundyNumbers[i - f]);
            }
            while (s.count(grundyNumbers[i])) grundyNumbers[i]++;
        }
    }

    void calc_bit() {
        for (int i = 1; i <= n; i++) {
            unsigned bit = 0;
            for (const auto &f: fib) {
                if (f > i) break;
                bit |= 1U << grundyNumbers[i - f];
            }
            grundyNumbers[i] = __builtin_ctz(~bit);
        }
    }

    int operator[](int i) { return grundyNumbers[i]; }

};

int32_t main() {
    fastIO;
    fib = {1, 1};
    for (int i = 2; fib[i - 1] + fib[i - 2] < 3'000'000; i++) fib.emplace_back(fib[i - 1] + fib[i - 2]);

    int n;
    cin >> n;

    SpragueGrundy solver(3'000'000);
    solver.calc_bit();

    int ans = 0, e;
    while (n--) {
        cin >> e;
        ans ^= solver[e];
    }

    cout << (ans ? "koosaga" : "cubelover");

    return 0;
}